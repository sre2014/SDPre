public class Objective8Lab1 {
  public static void main(String[] args) {
    // Write a for loop that will print the numbers 1 to 10
    for(int i = 1; i <= 10; i++) {
      System.out.println(i);
    }

    // Write another for loop that will print the numbers 10 to 1
    for(int i = 10; i >= 1; i--) {
      System.out.println(i);
    }
  }
}
